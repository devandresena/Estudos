public class Veiculo {
    String modelo;
    String tipo;
    String fabricante;
    int anoFabricacao;

    public Veiculo(String modelo, String tipo, String fabricante, int anoFabricacao) {
        this.modelo = modelo;
        this.tipo = tipo;
        this.fabricante = fabricante;
        this.anoFabricacao = anoFabricacao;
    }
}
